    private void paymentBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paymentBTNActionPerformed
if((DebitcardjRadioButton.isSelected())||(CreditcardjRadioButton.isSelected()))
{
String card=Cardnumber.getText();
String cvv=new String(CVV.getPassword());
String M=ExpiryMonth.getText();
String Y="20"+ExpiryYear.getText();
int m=Integer.parseInt(M);
int y=Integer.parseInt(Y);
int l1=card.length();
int l2=cvv.length();
Calendar c=Calendar.getInstance();
int month=c.get(Calendar.MONTH)+1;
int year=c.get(Calendar.YEAR);
if(l1!=12 || l2!=3)
{Cardnumber.setText("");
CVV.setText("");
ExpiryMonth.setText("");
ExpiryYear.setText("");
JOptionPane.showMessageDialog(this,"Enter valid card details");
}
   else if(y<year)
       JOptionPane.showMessageDialog(this,"Your card has Expired");
   else if(y==year&&m<month)
       JOptionPane.showMessageDialog(this,"Your card has Expired");
   else
    {
     Final.setVisible(true);
    }
}
  else if((NetbankingjRadioButton.isSelected())||(PaytmjRadioButton.isSelected()))
 {
  Final.setVisible(true);  
 }
    }//GEN-LAST:event_paymentBTNActionPerformed

