    private void walletBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_walletBTNActionPerformed
String s=(String)ownerIDTF.getText();
int s1=Integer.parseInt(numberTF.getText());
if(!(s.equals("1") || s.equals("2") || s.equals("3") || s.equals("4") || s.equals("5"))){
    JOptionPane.showMessageDialog(null,"Please enter a valid team ID!");
}
else{
if(s1<=0 || s1>12){
    JOptionPane.showMessageDialog(null,"Please enter a valid number of players!");
}
else{
int prevBid=1500000*s1;
try{
Class.forName("java.sql.Driver");
Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/ipl","root","Dragon@2002");
Statement stmt=con.createStatement();
      String query="select * from wallet where ownerid='"+s+"';";
      ResultSet rs=stmt.executeQuery(query);
        if(rs.next()){
            int wallet=rs.getInt("Wallet");
            walletOldLB.setText(""+wallet);
            walletNewLB.setText(""+(wallet-prevBid));
        }
        }
catch(Exception e){
    JOptionPane.showMessageDialog(null,e);
}
}
}
    }//GEN-LAST:event_walletBTNActionPerformed

